"""
CPU-bound обработка курса валюты.

Функция выполняется в отдельном процессе (ProcessPoolExecutor), поэтому
она должна быть простой, без сторонних объектов (только сериализуемые
через pickle аргументы и результат) и не зависеть от состояния основного
процесса.

Имитация "тяжёлых" вычислений: для каждого полученного курса генерируется
выборка значений (нормальное распределение вокруг курса) и по ней
рассчитываются среднее и стандартное отклонение через numpy. Это типичная
CPU-bound нагрузка (например, статистический анализ временного ряда котировок).
"""

from __future__ import annotations

import time

import numpy as np

# Размер выборки подобран так, чтобы обработка одного элемента занимала
# заметное время (десятки миллисекунд) и суммарная нагрузка на 50 элементов
# давала измеримое ускорение от использования нескольких процессов.
SAMPLE_SIZE = 400_000


def process_rate(raw: dict) -> dict:
    """
    CPU-bound расчёт: среднее и стандартное отклонение курса.

    Принимает "сырые" данные из этапа 1 (asyncio) и возвращает запись,
    готовую для сохранения в БД на этапе 3.
    """
    rate = float(raw["rate"])

    # Генерируем синтетическую выборку "колебаний" курса вокруг
    # полученного значения и считаем статистики - это и есть CPU-bound
    # часть, которая выигрывает от выполнения в отдельном процессе (без GIL).
    rng = np.random.default_rng()
    samples = rng.normal(loc=rate, scale=max(rate * 0.01, 1e-6), size=SAMPLE_SIZE)

    average_rate = float(np.mean(samples))
    std_dev = float(np.std(samples))

    return {
        "timestamp": time.time(),
        "currency_pair": raw["pair"],
        "average_rate": average_rate,
        "std_dev": std_dev,
        "source": raw["kind"],
        "cid": raw["cid"],
    }
